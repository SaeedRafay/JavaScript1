// If x equals 7, and the only other statement is x = x % 3, what would be the value of x after the calculation?
// Answer: The final value of x would be 1 because 7 % 3 would give the remainder if 7 is divided by 3.
let x = 7;
x = x % 3;
console.log("The value of x was 7 and then if 7 is divided by 3, the remainder would be: " + x);

